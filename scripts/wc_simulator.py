def simulate_match_fifa(home_fifa_pts, away_fifa_pts):
    """
    Simulate a single match using FIFA points model.
    Returns (home_score, away_score, result).
    """
    home_params = scale_params_by_fifa_points(home_fifa_pts)
    away_params = scale_params_by_fifa_points(away_fifa_pts)

    _, home_goals = simulate_team_match(*home_params)
    _, away_goals = simulate_team_match(*away_params)

    if home_goals > away_goals:
        result = 'home_win'
    elif away_goals > home_goals:
        result = 'away_win'
    else:
        result = 'draw'

    return int(home_goals), int(away_goals), result

def simulate_group_stage_matches_fifa(matches_df, teams_df):
    """
    Simulate scores for all unplayed group stage matches using the FIFA
    points model. Each team's shots and shot quality are derived from
    their FIFA ranking points via scale_params_by_fifa_points().

    Only matches with stage_id == 1 and status == 'scheduled' are simulated.
    Completed matches are intentionally excluded to preserve real results.

    Companion function to simulate_group_stage_matches(). Use this when
    running the FIFA strength model. Use simulate_group_stage_matches()
    for the baseline equal-probability Poisson model.

    Args:
        matches_df (pd.DataFrame): Full matches table from DB (all 104 rows).
        teams_df (pd.DataFrame): Teams table from DB. Must contain columns:
            id, fifa_points. All 48 teams must have non-null fifa_points
            before calling this function.

    Returns:
        pd.DataFrame: Copy of the scheduled group stage rows with columns added:
            home_score (int), away_score (int), goal_diff (int),
            result (str: 'home_win' | 'away_win' | 'draw'),
            status (str: 'simulated')

    Raises:
        ValueError: if any team in the scheduled matches has a null fifa_points,
                    or if a team_id in a scheduled match is not found in teams_df.
        AssertionError: if no scheduled group stage matches are found,
                        or if any score values are null after simulation.
    """

    # ── Guard: confirm all teams have fifa_points populated ──────────────
    missing = teams_df[teams_df['fifa_points'].isna()]['team_name'].tolist()
    if missing:
        raise ValueError(
            f"Cannot run FIFA model — missing fifa_points for: {missing}. "
            "Run fifa_points_update.sql to populate all 48 teams."
        )

    # ── Build a fast lookup dict: {team_id: fifa_points} ─────────────────
    fifa_pts_lookup = teams_df.set_index('id')['fifa_points'].to_dict()

    # ── Filter to unplayed group stage matches ────────────────────────────
    group_stage_matches = matches_df[
        (matches_df['stage_id'] == 1) &
        (matches_df['status'] == 'scheduled')
    ].copy()

    assert len(group_stage_matches) > 0, (
        "No scheduled group stage matches found. "
        "Check that stage_id == 1 and status == 'scheduled' rows exist in matches_df."
    )

    # ── Simulate each match ───────────────────────────────────────────────
    home_scores = []
    away_scores = []
    results     = []

    for _, row in group_stage_matches.iterrows():
        home_pts = fifa_pts_lookup.get(row['home_team_id'])
        away_pts = fifa_pts_lookup.get(row['away_team_id'])

        # Guard against a team_id present in matches but missing from teams table
        if home_pts is None:
            raise ValueError(
                f"Match id={row['id']}: home_team_id={row['home_team_id']} "
                "not found in teams_df. Check the teams table for missing rows."
            )
        if away_pts is None:
            raise ValueError(
                f"Match id={row['id']}: away_team_id={row['away_team_id']} "
                "not found in teams_df. Check the teams table for missing rows."
            )

        # Derive shot distribution parameters from each team's FIFA points
        home_params = scale_params_by_fifa_points(home_pts)
        away_params = scale_params_by_fifa_points(away_pts)

        # Simulate each team's performance independently
        _, home_goals = simulate_team_match(*home_params)
        _, away_goals = simulate_team_match(*away_params)

        # Cast to plain int — numpy int64 can cause issues downstream
        home_goals = int(home_goals)
        away_goals = int(away_goals)

        # Determine match result
        if home_goals > away_goals:
            result = 'home_win'
        elif away_goals > home_goals:
            result = 'away_win'
        else:
            result = 'draw'

        home_scores.append(home_goals)
        away_scores.append(away_goals)
        results.append(result)

    # ── Write results back to the DataFrame ──────────────────────────────
    group_stage_matches['home_score'] = home_scores
    group_stage_matches['away_score'] = away_scores
    group_stage_matches['goal_diff']  = (
        group_stage_matches['home_score'] - group_stage_matches['away_score']
    )
    group_stage_matches['result'] = results
    group_stage_matches['status'] = 'simulated'

    # ── Post-simulation assertions ────────────────────────────────────────
    assert group_stage_matches['home_score'].isna().sum() == 0, \
        "home_score has null values after FIFA model simulation."
    assert group_stage_matches['away_score'].isna().sum() == 0, \
        "away_score has null values after FIFA model simulation."

    logger.debug(
        "FIFA model simulated %d group stage matches",
        len(group_stage_matches)
    )

    return group_stage_matches


