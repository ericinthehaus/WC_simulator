def initial_ranking(final_standings: pd.DataFrame) -> pd.DataFrame:
    """Do an initial rank on points.
    Totals have already been calculated in the final_standings table, so we just need to sort by points, then goal difference, then goals for.

    Args:
        final_standings (DataFrame): generated from function `create_final_standings()`

    Returns:
        rankings (DataFrame): table with ranking based on points, goal difference, goals for
    """
    df = final_standings.copy()

    # get min rank based on points, then goal difference, then goals for
    # highest points gets rank 1, ties gets same rank, next rank is skipped
    df['overall_rank'] = df['points'].rank(method='min', ascending=False) 
    return df


def create_final_standings(group_stage_matches, teams_df):
    """
    Aggregate group-stage match results into final standings per team.
    
    Computes for each team:
    - points (3 for W, 1 for D, 0 for L)
    - goals_for (total goals scored)
    - goals_against (total goals conceded)
    - goal_diff (goals_for - goals_against)
    - matches_played
    - wins, draws, losses
    - head_to_head opponents (for tiebreaker logic)
    
    Args:
        group_stage_matches (pd.DataFrame): Simulated matches from function `simulate_group_stage_matches()` 
        teams_df (pd.DataFrame): Teams table with id, team_name, group columns
    
    Returns:
        pd.DataFrame: Final standings with one row per team, columns:
            team_id, team_name, group_id, matches_played, 
            points, goals_for, goals_against, goal_diff, opponents_beat, opponents_drew_with
    """
    
    standings_data = []
    
    for team_id in teams_df['id'].unique():
        team_name = teams_df[teams_df['id'] == team_id]['team_name'].iloc[0]
        group = teams_df[teams_df['id'] == team_id]['group_letter'].iloc[0]
        
        # Filter matches where this team played (home or away)
        home_matches = group_stage_matches[group_stage_matches['home_team_id'] == team_id]
        away_matches = group_stage_matches[group_stage_matches['away_team_id'] == team_id]
        
        # Aggregate stats 
        wins = len(home_matches[home_matches['result'] == 'home_win']) + \
               len(away_matches[away_matches['result'] == 'away_win'])
        draws = len(home_matches[home_matches['result'] == 'draw']) + \
                len(away_matches[away_matches['result'] == 'draw'])
        losses = len(home_matches[home_matches['result'] == 'away_win']) + \
                 len(away_matches[away_matches['result'] == 'home_win'])
        
        goals_for = home_matches['home_score'].sum() + away_matches['away_score'].sum()
        goals_against = home_matches['away_score'].sum() + away_matches['home_score'].sum()
        goal_diff = goals_for - goals_against
        
        points = wins * 3 + draws * 1
        matches_played = len(home_matches) + len(away_matches)
        
        # Track opponents for H2H tiebreaker
        opponents_beat = list(home_matches[home_matches['result'] == 'home_win']['away_team_id'].values) + \
                         list(away_matches[away_matches['result'] == 'away_win']['home_team_id'].values)
        opponents_drew_with = list(home_matches[home_matches['result'] == 'draw']['away_team_id'].values) + \
                              list(away_matches[away_matches['result'] == 'draw']['home_team_id'].values)
        
        standings_data.append({
            'team_id': team_id,
            'team_name': team_name,
            'group': group,
            'matches_played': matches_played,
            'wins': wins,
            'draws': draws,
            'losses': losses,
            'points': points,
            'goals_for': goals_for,
            'goals_against': goals_against,
            'goal_diff': goal_diff,
            'opponents_beat': opponents_beat,
            'opponents_drew_with': opponents_drew_with,
        })
    
    return pd.DataFrame(standings_data)


def find_tied_teams(standings, rank_column='overall_rank'):
    """
    Identify teams tied on a given rank column within a group.
    
    Returns teams grouped by their rank value so tied teams can be processed together.
    
    Args:
        standings (pd.DataFrame): Standings table with columns: group, team_id, team_name, and rank_column
        rank_column (str): Name of the column to check for ties (default 'overall_rank')
    
    Returns:
        dict: Mapping of {rank_value: list of team_ids at that rank}
              Only includes ranks where 2+ teams are tied.
    
    Example:
        >>> ties = find_tied_teams(standings, group='A', rank_column='overall_rank')
        >>> # Result: {1: [4, 7], 3: [12, 15, 18]}  (2 teams tied for 1st) (3 teams tied for 3rd) 
    """
    group_standings = standings.copy()
    
    # Find which rank values have more than one team
    rank_counts = group_standings[rank_column].value_counts()
    tied_ranks = rank_counts[rank_counts > 1].index.tolist()
    
    # Build a dict: {rank_value: [list of team_ids at that rank]}
    tied_teams_dict = {}
    for rank_val in tied_ranks:
        tied_teams = group_standings[group_standings[rank_column] == rank_val]['team_id'].tolist()
        tied_teams_dict[rank_val] = tied_teams
    
    return tied_teams_dict




