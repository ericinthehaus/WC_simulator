def simulate_group_stage_matches(matches_df: pd.DataFrame, mean_goals: str=MEAN_GOALS) -> pd.DataFrame:
    """ Filter for scheduled matches (not completed yet) and simulate them using the function `generate_goals_scored()`

    Args:
        matches_df (DataFrame): get from load_data()
        mean_goals (str): default to MEAN_GOALS constant

    Returns:
        group_stage_matches (DataFrame): 
    
    """
    dist = create_poisson_dist(mean_goals)

    group_stage_matches = matches_df[
        (matches_df['stage_id'] == 1) & (matches_df['status'] == 'scheduled')
        ].copy()  # Only group stage matches and that not completed yet.
    
    group_stage_matches['home_score'] = group_stage_matches.apply(lambda row: generate_goals_scored(dist), axis=1)
    assert group_stage_matches['home_score'].isnull().sum() == 0, "Error: home_score has null values after simulation"
    
    group_stage_matches['away_score'] = group_stage_matches.apply(lambda row: generate_goals_scored(dist), axis=1)
    assert group_stage_matches['away_score'].isnull().sum() == 0, "Error: away_score has null values after simulation"
    
    group_stage_matches['goal_diff'] = group_stage_matches['home_score'] - group_stage_matches['away_score']
    group_stage_matches['result'] = group_stage_matches.apply(lambda row: 'home_win' if row['goal_diff'] > 0 else ('draw' if row['goal_diff'] == 0 else 'away_win'), axis=1)    
    assert group_stage_matches['result'].isnull().sum() == 0, "Error: result has null values after simulation"  
    
    group_stage_matches['status'] = 'simulated'

    return group_stage_matches

