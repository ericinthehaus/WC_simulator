def load_data(db_path=DB_PATH):
    """Load matches and teams from the SQLite database."""
    conn = sqlite3.connect(db_path)
    matches_df = pd.read_sql_query("SELECT * FROM matches", conn)
    teams_df = pd.read_sql_query("SELECT * FROM teams", conn)
    conn.close()
    return matches_df, teams_df

def create_poisson_dist(mean_goals):
    """Returns a Poisson distribution object with the given mean."""
    return poisson(mu=mean_goals)

def generate_goals_scored(poisson_dist):
    """Returns an integer generated by a random draw from the given Poisson distribution."""
    return poisson_dist.rvs()

import numpy as np

def scale_params_by_fifa_points(fifa_points):
    """
    Translates raw FIFA points directly into normal and beta distribution parameters.
    """
    points = max(1100.0, min(1880.0, float(fifa_points)))
    scale_coef = (points - 1100.0) / (1880.0 - 1100.0)
    
    # Baseline shot properties before match-specific tactical suppression
    shot_mean = 7.0 + (10.0 * scale_coef)
    shot_std = 3.0 + (2.0 * scale_coef)
    
    # Shot quality (Beta parameters)
    target_mean_xg = 0.105
    target_variance = 0.024 - (0.004 * scale_coef)
    
    moments_factor = ((target_mean_xg * (1.0 - target_mean_xg)) / target_variance) - 1.0
    beta_alpha = target_mean_xg * moments_factor
    beta_beta = (1.0 - target_mean_xg) * moments_factor
    
    return shot_mean, shot_std, beta_alpha, beta_beta

def simulate_team_match(shot_mean, shot_std, beta_alpha, beta_beta):
    """
    Simulates a single team's match performance.
    """
    num_shots = int(np.round(np.random.normal(shot_mean, shot_std)))
    num_shots = max(0, num_shots)
    
    if num_shots == 0:
        return 0.0, 0
        
    shot_xg_values = np.random.beta(beta_alpha, beta_beta, size=num_shots)
    total_team_xg = np.sum(shot_xg_values)
    
    random_rolls = np.random.random(size=num_shots)
    goals_scored = np.sum(random_rolls < shot_xg_values)
    
    return total_team_xg, goals_scored

def apply_matchup_suppression(a_mean, a_std, b_mean, b_std):
    """
    NEW: Adjusts shot parameters based on the mutual strength of the teams.
    If both teams are elite, they restrict each other's attacking space.
    """
    # If the combined baseline shots exceed a standard open match baseline (e.g., 24.0)
    combined_base_shots = a_mean + b_mean
    
    if combined_base_shots > 24.0:
        # Scale suppression power. The more elite the matchup, the tighter the game.
        # Max suppression caps at around an 18% reduction for Rank 1 vs Rank 1.
        excess = combined_base_shots - 24.0
        suppression_factor = 1.0 - (0.018 * excess) 
        
        a_mean *= suppression_factor
        a_std *= suppression_factor
        b_mean *= suppression_factor
        b_std *= suppression_factor
        
    return a_mean, a_std, b_mean, b_std


